public class Forloop {
  
 
    public static void main(String[] args)
    {
   
        int[] arr = {3, 20, 25, 12,22};
  
        System.out.print("Elements of given array are: ");
  
        for (int i = 0; i < arr.length; i++) 
        {      
            System.out.print(arr[i] + " ");
        }
    }
}